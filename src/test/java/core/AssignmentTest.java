package core;

public class AssignmentTest {

	public static void main(String[] args) {
		
		// Assignment
		int x = 5;
		System.out.println(x);
	    
	    
		// Arithmatic assignment
		x += 3;
	    System.out.println(x);
	    
	    
	    // Subtraction assignment
	    x -= 3;
	    System.out.println(x);
	    
	    // Multiplication assignment
	    x *= 3;
	    System.out.println(x);
	    
	    // Division
	    x /= 3;
	    System.out.println(x);
	    
	    // Modulo
	    x %= 3;
	    System.out.println(x);
	    
	    // &
	    x &= 3;
	    System.out.println(x);
	    
	    // |
	    x |= 3;
	    System.out.println(x);
	    
	    // ^ 
	    x ^= 3;
	    System.out.println(x);
	    
	    // >>
	    x >>= 3;
	    System.out.println(x);
	    
	    // << 
	    x <<= 3;
	    System.out.println(x);
	   	

	}

}
