package core;

public class VariableTypes {

	public static void main(String[] args) {
		
		
		// All together
		int myNum = 5;               // Integer (whole number)
		float myFloatNum = 5.99f;    // Floating point number
		double myDouble= 12.34d;     // Double
		char myLetter = 'D';         // Character
		boolean myBool = true;       // Boolean
		boolean isFishTasty = false; // Boolean
		String myText = "Hello";     // String
		
		
		// int
		int a = 1;
		System.out.println("Value of int: " + a);
		
		// float
		float b = 1.2f;
		System.out.println("Value of float: " + b);
		
		// double
		double d = 1.2d;
		double num = 1.2;
		System.out.println("Value of double with suffix: " + d);
		System.out.println("Value of double without suffix: " + num);
		
		// String
		String greetings = "Hi";
		System.out.println("Value of greetings: " + greetings);
		
		// Char
		char c = 'c';
		System.out.println("Value of c: " + c);
		
		
		// Multiple variables with multiple values
		int x = 5;
		int y = 6;
		int z = 7;
		System.out.print("Addition of x, y & z is: ");
		System.out.println(x+y+z);
		
		//Multiple variables with single value
		int u,v,w;
		u = v = w = 0;
		System.out.print("Addition of u, v & w is: ");
		System.out.println(u+v+w);
		
		
		
		
		
		
		

	}

}
