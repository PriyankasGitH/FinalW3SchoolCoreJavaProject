package core;

public class Arithmatic {

	public static void main(String[] args) {
		
		 // Addition 
		 int x = 5;
		 int y = 3;
		 System.out.println(x + y);
		
		 
		 // Subtraction
		 System.out.println(x - y);
		 
		 
	     // Multiplication
		 System.out.println(x * y);
		 
		 
		 // Division
		 System.out.println(x / y);
		 
		 
		 //Modulous
		 System.out.println(x % y);
		 
		 
		 //Increment
		 ++x;
		 System.out.println(x);
		 
		//Decrement
		 --x;
		 System.out.println(x);
		 
		 
	}

}
