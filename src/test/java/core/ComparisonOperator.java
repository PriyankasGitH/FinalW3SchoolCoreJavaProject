package core;

public class ComparisonOperator {

	public static void main(String[] args) {
		 
		 int x = 5;
		 int y = 3;
		 
		 // Less than or equal to
		 System.out.println(x <= y);
		 
		 // Greater than or equal to
		 System.out.println(x >= y);
		 
		 // Less than 
		 System.out.println(x < y);
		
		 // Greater than 
		 System.out.println(x > y);
		 
		 // Not equal to 
		 System.out.println(x != y);
		 
		 // Equal than 
		 System.out.println(x == y);

	}

}
