package realLifeExamples;

public class DoWhileTest {

	public static void main(String[] args) {
		
		
		//while loop, we have created a simple "count down" program:
		int countdown = 3;

		while (countdown > 0) {
		  System.out.println(countdown);
		  countdown--;
		}

		System.out.println("Happy New Year!!");
		
		
	}

}
